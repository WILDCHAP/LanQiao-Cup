//10个大数字相加
void add(char *a0, char *a1, char *a2, char *a3, char *a4, char *a5, char *a6, char *a7, char *a8, char *a9, char *ans)
{
	int k0[22] = { 0 }, k1[22] = { 0 }, k2[22] = { 0 }, k3[22] = { 0 }, k4[22] = { 0 }, k5[22] = { 0 }, k6[22] = { 0 }, k7[22] = { 0 }, k8[22] = { 0 }, k9[22] = { 0 };
	int p = 20;
	for (int q = 0; q < 21; q++)
	{
		k0[q] = a0[p]-'0';
		k1[q] = a1[p]-'0';
		k2[q] = a2[p]-'0';
		k3[q] = a3[p]-'0';
		k4[q] = a4[p]-'0';
		k5[q] = a5[p]-'0';
		k6[q] = a6[p]-'0';
		k7[q] = a7[p]-'0';
		k8[q] = a8[p]-'0';
		k9[q] = a9[p]-'0';
		p--;
	}
	char *b = new char[22];
	int w = 0; //进位
	for (p = 0; p < 21; p++)
	{
		int temp = w + k0[p] + k1[p] + k2[p] + k3[p] + k4[p] + k5[p] + k6[p] + k7[p] + k8[p] + k9[p];
		w = temp / 10;
		b[p] = temp % 10+'0';
	}
	if (w != 0)
	{
		ans[0] = '0';  //22位，和不是21位数
		ans[1] = '\0';
		return;
	}
	for (p = 20; w < 21; w++)
	{
		ans[w] = b[p--];
	}
	ans[w] = '\0';
	return;
}

